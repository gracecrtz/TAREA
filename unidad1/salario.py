#Calcular el nuevo salario de un obrero si obtuvo un incremento del 25% de su salario anterior.


#Ingresar el dato 
sal=float(input("Ingresar el salario anterior del trabajdor "))
inc=.25

#operaciones
op=sal*inc
res=sal+op

#Imprimir 
print(f"El nuevo salario del trabajador es {res}")