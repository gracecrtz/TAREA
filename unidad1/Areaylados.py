#algoritmo que calcula el Area de un rectangulo si la altura y el lado es N

#Ingresar la base y altura del rectangulo
lado=float(input("Ingrese el lado del rectangulo "))
base=float(input("Ingrese la base del rectangulo "))

#operaciones
area=lado*base

#imprimir el area del rectangulo 
print(f"el area del rectangulo con base {base:.0f} y lado {lado:.0f} es igual a {area:.0f}")
